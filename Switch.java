public class Switch {
    
    public static void main(String args[]){
         int n = 3;

        switch(n){
            case 1:
                System.out.println("Monday");
                break;
             case 2:
                    System.out.println("tuesday");
                    break;
             case 3:
                     System.out.println("wensday");
                      break;
             case 4:
                     System.out.println("thersday");
                    break;
             default :
                    System.out.println("Enter valid number");
        }

        
    }
}
